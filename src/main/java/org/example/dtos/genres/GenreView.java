package org.example.dtos.genres;

public class GenreView {
    private Long id;
    private String name;

    public GenreView() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
